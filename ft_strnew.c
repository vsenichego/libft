#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *s;

	s = malloc(size);
	if (!s)
		return (NULL);
	while (size)
	{
		s[size] = '\0';
		size--;
	}
	s[0] = '\0';
	return (s);
}
